using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Character_project
{
    class character
    {
        public string name;
        public string eyecolor;
        public string haircolor;
        public string skincolor;
        public int age;
        public character(string name, string eyecolor, string haircolor, string skincolor, int age)
        {
            this.name = name;
            this.eyecolor = eyecolor;
            this.haircolor = haircolor;
            this.skincolor = skincolor;
            this.age = age; 


        }

    }
}
